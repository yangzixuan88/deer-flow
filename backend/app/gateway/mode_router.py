# Core entry point
# ---------------------------------------------------------------------------

def ensure_mode_decision(
    payload: dict[str, Any] | None,
    context_id: str | None = None,
    request_id: str | None = None,
    source_system: str = "gateway",
) -> ModeDecision:
    """
    Extract existing or generate new ModeDecision from payload.

    If payload already contains a valid mode_decision dict / ModeDecision,
    it is validated and returned (not overwritten).

    R240-5 constraint: do NOT overwrite existing valid ModeDecision.
    """
    if payload is None:
        payload = {}

    # Case 1: existing mode_decision
    existing = payload.get("mode_decision")
    if existing is not None:
        if isinstance(existing, ModeDecision):
            return existing
        if isinstance(existing, dict):
            try:
                return ModeDecision.from_dict(existing)
            except Exception:
                logger.warning("[R240-5] Invalid mode_decision in payload, regenerating")

    # Case 2: no existing — generate from rules
    raw_input = payload.get("raw_input") or payload.get("input", {}).get("messages", [{}])[-1].get("content", "") if isinstance(payload.get("input"), dict) else str(payload.get("raw_input", ""))

    # Fallback: try to get from body.input.messages
    if not raw_input and isinstance(payload.get("input"), dict):
        msgs = payload["input"].get("messages")
        if msgs and isinstance(msgs, list):
            last = msgs[-1]
            if isinstance(last, dict):
                raw_input = last.get("content", "")

    candidates = infer_candidate_modes(raw_input)
    selected_mode, priority_rule, needs_clar = apply_mode_priority_rules(candidates, raw_input)

    # Determine delegation
    delegated_to = _DELEGATION_DEFAULTS.get(selected_mode, DelegatedTo.NONE)

    # Determine result_sink defaults per mode
    result_sink = ModeResultSink()
    if selected_mode == SelectedMode.GOVERNANCE_REVIEW:
        result_sink.governance = True
        result_sink.runtime_artifact = False
    elif selected_mode == SelectedMode.ROUNDTABLE:
        result_sink.frontend_thread = True
        result_sink.governance = True
    elif selected_mode == SelectedMode.AUTONOMOUS_AGENT:
        result_sink.runtime_artifact = True
        result_sink.nightly_review = True

    decision = ModeDecision(
        mode_decision_id=generate_mode_decision_id(),
        request_id=request_id,
        context_id=context_id,
        selected_mode=selected_mode,
        candidate_modes=candidates,
        confidence=candidates[0].confidence if candidates else 0.5,
        decision_reason=candidates[0].reason if candidates else "default",
        priority_rule_applied=priority_rule,
        needs_clarification=needs_clar,
        delegated_to=delegated_to,
        fallback_mode=SelectedMode.DIRECT_ANSWER,
        allowed_tools=[],
        denied_tools=[],
        state_scope=ModeStateScope(),
        result_sink=result_sink,
        created_at=datetime.now(UTC).isoformat(),
        source_system=source_system,
    )
    return decision


def inject_mode_decision_into_context(
    context: dict[str, Any] | None,
    decision: ModeDecision,
) -> dict[str, Any]:
    """
    Inject ModeDecision into context dict (non-destructive).
    """
    if context is None:
        context = {}
    context["mode_decision"] = decision.to_dict()
    return context


def extract_mode_decision_from_context(
    context: dict[str, Any] | None,
) -> ModeDecision | None:
    """
    Extract ModeDecision from context dict (if present).
    """
    if context is None:
        return None
    raw = context.get("mode_decision")
    if raw is None:
        return None
    if isinstance(raw, ModeDecision):
        return raw
    if isinstance(raw, dict):
        try:
            return ModeDecision.from_dict(raw)
        except Exception:
            return None
    return None


def mode_decision_summary(decision: ModeDecision) -> str:
    """
    Safe log summary — masks IDs, no raw_input.
    """
    def _mask(s: str | None, chars: int = 8) -> str:
        if s is None:
            return "None"
        return f"{s[:chars]}..."
    return (
        f"mode={decision.selected_mode.value} "
        f"conf={decision.confidence:.2f} "
        f"delegated={decision.delegated_to.value} "
        f"needs_clar={decision.needs_clarification} "
        f"md_id={_mask(decision.mode_decision_id)} "
        f"ctx_id={_mask(decision.context_id)}"
    )
