"""
Mode Router — R240-5 Minimal Wrapper
=====================================
在现有路由逻辑不改变的前提下，统一生成 ModeDecision metadata。

约束（绝对禁止改变）：
- 任何 M01 / M04 / RTCM / DeerFlow 的实际路由行为
- 任何现有 API 响应结构
- 任何 Governance actual/predicted 语义

设计原则：只包装，不抢权。ModeDecision 是 metadata，不是执行引擎。
"""

from __future__ import annotations

import logging
import time
from dataclasses import dataclass, field
from datetime import UTC, datetime
from enum import StrEnum
from typing import Any

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Selected Mode Enum — matches R240-2 ModeDecision.schema.json
# ---------------------------------------------------------------------------

class SelectedMode(StrEnum):
    SEARCH = "search"
    TASK = "task"
    WORKFLOW = "workflow"
    AUTONOMOUS_AGENT = "autonomous_agent"
    ROUNDTABLE = "roundtable"
    DIRECT_ANSWER = "direct_answer"
    CLARIFICATION = "clarification"
    GOVERNANCE_REVIEW = "governance_review"
    MIXED_MODE = "mixed_mode"


class DelegatedTo(StrEnum):
    M01_ORCHESTRATOR = "m01_orchestrator"
    M04_COORDINATOR = "m04_coordinator"
    DEERFLOW_HARNESS = "deerflow_harness"
    PYTHON_GATEWAY = "python_gateway"
    RTCM_MAIN_AGENT_HANDOFF = "rtcm_main_agent_handoff"
    GOVERNANCE_BRIDGE = "governance_bridge"
    FRONTEND_ONLY = "frontend_only"
    CHANNEL_MANAGER = "channel_manager"
    NONE = "none"


# ---------------------------------------------------------------------------
# Delegation defaults — from R240-2 ModePriorityRules.json
# ---------------------------------------------------------------------------

_DELEGATION_DEFAULTS: dict[SelectedMode, DelegatedTo] = {
    SelectedMode.SEARCH: DelegatedTo.M04_COORDINATOR,
    SelectedMode.TASK: DelegatedTo.M04_COORDINATOR,
    SelectedMode.WORKFLOW: DelegatedTo.M04_COORDINATOR,
    SelectedMode.AUTONOMOUS_AGENT: DelegatedTo.DEERFLOW_HARNESS,
    SelectedMode.ROUNDTABLE: DelegatedTo.RTCM_MAIN_AGENT_HANDOFF,
    SelectedMode.DIRECT_ANSWER: DelegatedTo.M01_ORCHESTRATOR,
    SelectedMode.CLARIFICATION: DelegatedTo.M01_ORCHESTRATOR,
    SelectedMode.GOVERNANCE_REVIEW: DelegatedTo.GOVERNANCE_BRIDGE,
    SelectedMode.MIXED_MODE: DelegatedTo.M01_ORCHESTRATOR,
}


# ---------------------------------------------------------------------------
# Candidate Mode record
# ---------------------------------------------------------------------------

@dataclass
class CandidateMode:
    mode: SelectedMode
    confidence: float  # 0.0–1.0
    reason: str


# ---------------------------------------------------------------------------
# State Scope — minimal view scoped to ModeDecision
# ---------------------------------------------------------------------------

@dataclass
class ModeStateScope:
    thread_id: str | None = None
    session_id: str | None = None
    task_id: str | None = None
    workflow_id: str | None = None
    rtcm_session_id: str | None = None
    governance_trace_id: str | None = None
    memory_scope: str = "session"


# ---------------------------------------------------------------------------
# Result Sink — minimal view scoped to ModeDecision
# ---------------------------------------------------------------------------

@dataclass
class ModeResultSink:
    frontend_thread: bool = True
    memory: bool = False
    asset: bool = False
    governance: bool = False
    runtime_artifact: bool = True
    nightly_review: bool = False


# ---------------------------------------------------------------------------
# ModeDecision — R240-5 minimal wrapper
# ---------------------------------------------------------------------------

@dataclass
class ModeDecision:
    """
    ModeDecision metadata — generated by rule-based heuristic.

    All fields optional/defaulted to keep backward compatibility.
    Does NOT change any existing routing/execution logic.
    """
    mode_decision_id: str | None = None
    request_id: str | None = None
    context_id: str | None = None
    selected_mode: SelectedMode = SelectedMode.DIRECT_ANSWER
    candidate_modes: list[CandidateMode] = field(default_factory=list)
    confidence: float = 0.5
    decision_reason: str = ""
    priority_rule_applied: str = ""
    needs_clarification: bool = False
    delegated_to: DelegatedTo = DelegatedTo.NONE
    fallback_mode: SelectedMode | None = None
    allowed_tools: list[str] = field(default_factory=list)
    denied_tools: list[str] = field(default_factory=list)
    state_scope: ModeStateScope = field(default_factory=ModeStateScope)
    result_sink: ModeResultSink = field(default_factory=ModeResultSink)
    created_at: str | None = None
    source_system: str = "gateway"

    def to_dict(self) -> dict[str, Any]:
        return {
            "mode_decision_id": self.mode_decision_id,
            "request_id": self.request_id,
            "context_id": self.context_id,
            "selected_mode": self.selected_mode.value if isinstance(self.selected_mode, SelectedMode) else self.selected_mode,
            "candidate_modes": [
                {"mode": c.mode.value, "confidence": c.confidence, "reason": c.reason}
                for c in self.candidate_modes
            ],
            "confidence": self.confidence,
            "decision_reason": self.decision_reason,
            "priority_rule_applied": self.priority_rule_applied,
            "needs_clarification": self.needs_clarification,
            "delegated_to": self.delegated_to.value if isinstance(self.delegated_to, DelegatedTo) else self.delegated_to,
            "fallback_mode": self.fallback_mode.value if isinstance(self.fallback_mode, SelectedMode) else self.fallback_mode,
            "allowed_tools": self.allowed_tools,
            "denied_tools": self.denied_tools,
            "state_scope": {
                "thread_id": self.state_scope.thread_id,
                "session_id": self.state_scope.session_id,
                "task_id": self.state_scope.task_id,
                "workflow_id": self.state_scope.workflow_id,
                "rtcm_session_id": self.state_scope.rtcm_session_id,
                "governance_trace_id": self.state_scope.governance_trace_id,
                "memory_scope": self.state_scope.memory_scope,
            },
            "result_sink": {
                "frontend_thread": self.result_sink.frontend_thread,
                "memory": self.result_sink.memory,
                "asset": self.result_sink.asset,
                "governance": self.result_sink.governance,
                "runtime_artifact": self.result_sink.runtime_artifact,
                "nightly_review": self.result_sink.nightly_review,
            },
            "created_at": self.created_at,
            "source_system": self.source_system,
        }

    @classmethod
    def from_dict(cls, data: dict) -> ModeDecision:
        if data is None:
            return cls()
        known = {}
        for key in [
            "mode_decision_id", "request_id", "context_id", "selected_mode",
            "confidence", "decision_reason", "priority_rule_applied",
            "needs_clarification", "delegated_to", "fallback_mode",
            "allowed_tools", "denied_tools", "created_at", "source_system",
        ]:
            if key in data:
                known[key] = data[key]

        # Parse enums
        if "selected_mode" in known and isinstance(known["selected_mode"], str):
            try:
                known["selected_mode"] = SelectedMode(known["selected_mode"])
            except ValueError:
                known["selected_mode"] = SelectedMode.DIRECT_ANSWER
        if "delegated_to" in known and isinstance(known["delegated_to"], str):
            try:
                known["delegated_to"] = DelegatedTo(known["delegated_to"])
            except ValueError:
                known["delegated_to"] = DelegatedTo.NONE
        if "fallback_mode" in known and isinstance(known["fallback_mode"], str):
            try:
                known["fallback_mode"] = SelectedMode(known["fallback_mode"])
            except ValueError:
                known["fallback_mode"] = None

        # Parse candidate_modes
        if "candidate_modes" in data and isinstance(data["candidate_modes"], list):
            known["candidate_modes"] = []
            for c in data["candidate_modes"]:
                if isinstance(c, dict):
                    try:
                        known["candidate_modes"].append(CandidateMode(
                            mode=SelectedMode(c.get("mode", "direct_answer")),
                            confidence=float(c.get("confidence", 0.5)),
                            reason=str(c.get("reason", "")),
                        ))
                    except (ValueError, TypeError):
                        pass

        # Parse state_scope
        if "state_scope" in data and isinstance(data["state_scope"], dict):
            ss = data["state_scope"]
            known["state_scope"] = ModeStateScope(
                thread_id=ss.get("thread_id"),
                session_id=ss.get("session_id"),
                task_id=ss.get("task_id"),
                workflow_id=ss.get("workflow_id"),
                rtcm_session_id=ss.get("rtcm_session_id"),
                governance_trace_id=ss.get("governance_trace_id"),
                memory_scope=ss.get("memory_scope", "session"),
            )

        # Parse result_sink
        if "result_sink" in data and isinstance(data["result_sink"], dict):
            rs = data["result_sink"]
            known["result_sink"] = ModeResultSink(
                frontend_thread=rs.get("frontend_thread", True),
                memory=rs.get("memory", False),
                asset=rs.get("asset", False),
                governance=rs.get("governance", False),
                runtime_artifact=rs.get("runtime_artifact", True),
                nightly_review=rs.get("nightly_review", False),
            )

        return cls(**{k: v for k, v in known.items() if v is not None})


# ---------------------------------------------------------------------------
# ID generator
# ---------------------------------------------------------------------------

def generate_mode_decision_id() -> str:
    return f"md-{time.time():.0f}-{__import__('uuid').uuid4().hex[:8]}"


