# ---------------------------------------------------------------------------
# Rule-based mode inference (no model call)
# ---------------------------------------------------------------------------

def _keyword_match(text: str, keywords: set[str]) -> bool:
    text_lower = text.lower()
    return any(kw in text_lower for kw in keywords)


def infer_candidate_modes(raw_input: str) -> list[CandidateMode]:
    """
    Infer candidate modes from raw_input using keyword heuristics.
    Returns list sorted by confidence descending.
    """
    candidates: list[tuple[SelectedMode, float, str]] = []
    text = raw_input.strip()

    # Short input → clarification
    if len(text) < 10:
        candidates.append((SelectedMode.CLARIFICATION, 0.85, "input too short"))

    # Governance keywords
    if _keyword_match(text, _GOVERNANCE_KEYWORDS):
        candidates.append((SelectedMode.GOVERNANCE_REVIEW, 0.90, "governance keyword match"))

    # Roundtable keywords
    if _keyword_match(text, _ROUNDTABLE_KEYWORDS):
        # Explicit roundtable → high confidence, no clarification needed
        candidates.append((SelectedMode.ROUNDTABLE, 0.95, "roundtable keyword match"))
    elif any(k in text.lower() for k in {"讨论", "评审", "会议", "圆桌", "council", "debate"}):
        candidates.append((SelectedMode.ROUNDTABLE, 0.70, "inferred roundtable — needs confirmation"))
        # Override: if only inferred, mark needs_clarification later

    # Autonomous agent
    if _keyword_match(text, _AUTONOMOUS_KEYWORDS):
        candidates.append((SelectedMode.AUTONOMOUS_AGENT, 0.85, "autonomous keyword match"))

    # Workflow
    if _keyword_match(text, _WORKFLOW_KEYWORDS):
        candidates.append((SelectedMode.WORKFLOW, 0.80, "workflow keyword match"))

    # Task
    if _keyword_match(text, _TASK_KEYWORDS):
        candidates.append((SelectedMode.TASK, 0.75, "task keyword match"))

    # Search
    if _keyword_match(text, _SEARCH_KEYWORDS):
        candidates.append((SelectedMode.SEARCH, 0.75, "search keyword match"))

    # Clarification triggers (代词 / ambiguous)
    if _keyword_match(text, _CLARIFICATION_TRIGGERS):
        if not any(c[0] == SelectedMode.CLARIFICATION for c in candidates):
            candidates.append((SelectedMode.CLARIFICATION, 0.65, "ambiguous pronoun detected"))

    # Direct answer fallback
    if not candidates:
        candidates.append((SelectedMode.DIRECT_ANSWER, 0.50, "default fallback"))

    # Sort by confidence descending
    candidates.sort(key=lambda x: x[1], reverse=True)

    return [
        CandidateMode(mode=mode, confidence=conf, reason=reason)
        for mode, conf, reason in candidates
    ]


# ---------------------------------------------------------------------------
# Priority rules — from R240-2 ModePriorityRules.json
# ---------------------------------------------------------------------------

def apply_mode_priority_rules(
    candidates: list[CandidateMode],
    raw_input: str | None = None,
) -> tuple[SelectedMode, str, bool]:
    """
    Apply R240-2 priority rules to select the final mode.

    Returns (selected_mode, priority_rule_applied, needs_clarification).
    """
    if not candidates:
        return SelectedMode.DIRECT_ANSWER, "no_candidates", False

    # Priority order from R240-2 (highest to lowest):
    # governance_review > clarification > roundtable >
    # autonomous_agent > workflow > task > search > direct_answer > mixed_mode
    _PRIORITY_ORDER = [
        SelectedMode.GOVERNANCE_REVIEW,
        SelectedMode.CLARIFICATION,
        SelectedMode.ROUNDTABLE,
        SelectedMode.AUTONOMOUS_AGENT,
        SelectedMode.WORKFLOW,
        SelectedMode.TASK,
        SelectedMode.SEARCH,
        SelectedMode.DIRECT_ANSWER,
        SelectedMode.MIXED_MODE,
    ]

    # Check if top mode has sufficient margin over second
    top = candidates[0]
    second_conf = candidates[1].confidence if len(candidates) > 1 else 0.0

    needs_clarification = False
    rule_applied = "top_candidate"

    # Rule: governance_review always wins
    if top.mode == SelectedMode.GOVERNANCE_REVIEW:
        rule_applied = "governance_over_ordinary_execution"
        needs_clarification = False

    # Rule: clarification for low confidence or short margin
    elif top.confidence < 0.65:
        needs_clarification = True
        rule_applied = "clarification_before_execution"

    elif len(candidates) > 1 and (top.confidence - second_conf) < 0.15:
        # Low margin → clarification
        needs_clarification = True
        rule_applied = "low_margin_clarification"

    # Rule: inferred roundtable needs confirmation
    elif top.mode == SelectedMode.ROUNDTABLE:
        if "roundtable" not in (raw_input or "").lower():
            # Only inferred, not explicit → needs clarification
            needs_clarification = True
            rule_applied = "suggested_roundtable_requires_confirmation"
        else:
            rule_applied = "explicit_roundtable"

    # Rule: short input → clarification
    elif raw_input and len(raw_input.strip()) < 10:
        needs_clarification = True
        rule_applied = "short_input_clarification"

    # Otherwise top candidate wins
    return top.mode, rule_applied, needs_clarification


